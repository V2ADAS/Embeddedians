#ifndef MUSART_INTERFACE_H_
#define MUSART_INTERFACE_H_


/*******************************************************************************************************/
/*                                       LIB                                                           */
/*******************************************************************************************************/
#include "MUSART_Private.h"
/********************************************************************************************************/
/*                                            macros of UARTx                                           */
/********************************************************************************************************/
#define UART1               1
#define UART2               2
#define UART6               6


/*******************************************************************************************************/
/*                                   USART LENGTH MODES : 8-BIT , 9-BIT                                */
/*******************************************************************************************************/
#define _8DATA_LENGTH_         0
#define _9DATA_LENGTH_         1


/******************************************************************************************************/
/*             USART STOP BIT MODES : 00 -> ONE_STOP , 10 -> TWO_STOP                                 */
/******************************************************************************************************/
#define USART1_ONE_STOP_BITS  00
#define USART1_TWO_STOP_BITS  10


/******************************************************************************************************/
/*                                          APIs                                                      */
/******************************************************************************************************/

/******************************************************************************************************/
/*                                      01- MUART_Enable                                              */
/*----------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function Enable UART                                                    */
/* 2- Function Input       -> @param UART_Index = UART1 , UART2 , UART6                               */                   
/* 3- Function Return      -> void                                                                    */
/******************************************************************************************************/
void MUART_Enable(u8 UART_Index);
/******************************************************************************************************/

/******************************************************************************************************/
/*                                      02- MUART_Disable                                             */
/*----------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function Disable UART                                                   */
/* 2- Function Input       -> @param UART_Index = UART1 , UART2 , UART6                               */                   
/* 3- Function Return      -> void                                                                    */
/******************************************************************************************************/
void MUART_Disable(u8 UART_Index);
/******************************************************************************************************/



/*******************************************************************************************************/
/*                                      03- MUART_Send_Byte                                            */
/*-----------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function Send 1 byte                                                     */
/* 2- Function Input       -> @param UART_Index = UART1 , UART2 , UART6                                */  
/*                            @param data = should be 1 byte                                           */                 
/* 3- Function Return      -> void                                                                     */
/*******************************************************************************************************/
void MUART_Send_Byte(u8 UART_Index ,u8 data);
/*******************************************************************************************************/



/*******************************************************************************************************/
/*                                      04- MUART_Send_Data                                            */
/*-----------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function Send Data (array , string , ...)                                */
/* 2- Function Input       -> @param UART_Index = UART1 , UART2 , UART6                                */  
/*                            @param data = Can be array , string or any data                          */                 
/* 3- Function Return      -> void                                                                     */
/*******************************************************************************************************/
void MUART_Send_Data(u8 UART_Index ,u8* data);
/*********************************************************************************************************/


/********************************************************************************************************/
/*                                      05- MUART_Receive_Byte                                          */
/*------------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function Receive Byte                                                     */
/* 2- Function Input       -> @param UART_Index = UART1 , UART2 , UART6                                 */  
/* 3- Function Return      -> 1 Byte                                                                    */
/********************************************************************************************************/
u8 MUART_Receive_Byte(u8 UART_Index);
/********************************************************************************************************/


/********************************************************************************************************/
/*                                      06- MUART_Receive_Data                                          */
/*------------------------------------------------------------------------------------------------------*/
/* 1- Function Description -> Function Receive Data                                                     */
/* 2- Function Input       -> @param UART_Index = UART1 , UART2 , UART6                                 */ 
/*                            @param Buffer[] = the received Data                                       */ 
/*                            @param max_size = size of array                                           */
/* 3- Function Return      -> 1 Byte                                                                    */
/********************************************************************************************************/
void MUART_Receive_Data(u8 UART_Index,u8 Buffer[] ,u8 max_size);
/********************************************************************************************************/


/*Helper Function*/
USART_REG Get_UART(u8 UART_Index);


#endif /* MUSART_INTERFACE_H_ */
