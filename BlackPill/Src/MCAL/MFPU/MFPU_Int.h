#ifndef MCAL_MFPU_MFPU_INT_H_
#define MCAL_MFPU_MFPU_INT_H_

/*******************************************************************************************************/
/* 												Includes       		                                   */
/*******************************************************************************************************/
#include "../../LIB/STD_TYPES.h"  // Standard Types Library

/*******************************************************************************************************/
/* 												Definitions   		                                   */
/*******************************************************************************************************/
// FPU register base address
#define SCB_CPACR  (*(volatile u32 *)0xE000ED88)

/*******************************************************************************************************/
/* 												Function Prototypes                                    */
/*******************************************************************************************************/

/*-----------------------------------------------------------------------------------------------------*/
/* Description: Enables the Floating Point Unit (FPU)                                                  */
/*                                                                                                     */
/* Notes:                                                                                              */
/* - This function sets appropriate bits in the SCB_CPACR register to enable the FPU.                  */
/*-----------------------------------------------------------------------------------------------------*/
void MFPU_Enable(void);

/*-----------------------------------------------------------------------------------------------------*/
/* Description: Disables the Floating Point Unit (FPU)                                                 */
/*                                                                                                     */
/* Notes:                                                                                              */
/* - This function clears appropriate bits in the SCB_CPACR register to disable the FPU.               */
/*-----------------------------------------------------------------------------------------------------*/
void MFPU_Disable(void);

/*******************************************************************************************************/
#endif /* MCAL_MFPU_MFPU_INT_H_ */
